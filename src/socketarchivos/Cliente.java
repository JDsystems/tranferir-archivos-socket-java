    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        final String rutaArchivo = ruta;

        try {
            final File archivo = new File(rutaArchivo);
            Socket cliente = new Socket("localhost", 5000);
            BufferedInputStream bis = new BufferedInputStream(new FileInputStream(archivo));
            BufferedOutputStream bos = new BufferedOutputStream(cliente.getOutputStream());
            DataOutputStream dos = new DataOutputStream(cliente.getOutputStream());
            byte[] num_bytes = new byte[40];
            if (archivo.getName().getBytes().length > num_bytes.length) {
                JOptionPane.showMessageDialog(null, "Archivo supera el numero de bytes permitido");
            } else {
                dos.writeUTF(archivo.getName());
                while ((in = bis.read(num_bytes)) != -1) {
                    bos.write(num_bytes, 0, in);
                }

            }

            bis.close();
            bos.close();

        } catch (Exception e) {
            System.err.println("excepcion" + e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

